public class Dayss {

    String runs;
    String code;

    public Dayss(String runs, String code) {
        this.runs = runs;
        this.code = code;
    }

    public String getRuns() {
        return runs;
    }

    public void setRuns(String runs) {
        this.runs = runs;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
